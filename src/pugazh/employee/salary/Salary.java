package pugazh.employee.salary;

public class Salary
{
	private long basePay;
	private long providentFund;
	
	public long getBasePay()
	{
		return basePay;
	}
	
	public void setBasePay(long basePay)
	{
		this.basePay = basePay;
	}
	
	public long getProvidentFund()
	{
		return providentFund;
	}
	
	public void setProvidentFund(long providentFund)
	{
		this.providentFund = providentFund;
	}
	
	  

}
